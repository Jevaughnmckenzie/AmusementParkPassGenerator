struct FullAddress {
    var streetAddress: String?
    var city: String?
    var state: String?
    var zipcode: String?
    
    init(streetAddress: String, city: String, state: String, zipcode: String) {
        self.streetAddress = streetAddress
        self.city = city
        self.state = state
        self.zipcode = zipcode
    }
}








































